public class Ingredient {
  
  private String name;
  private int id;
  private String color;
  private int value;
  private AlchemyMarker alchemyMarker;
  
  public Ingredient(String name, int id, String color, int value, AlchemyMarker alchemyMarker) {
    this.name = name;
    this.id = id;
    this.color = color;
    this.value = value;
    this.alchemyMarker = alchemyMarker;
  }

  
}
